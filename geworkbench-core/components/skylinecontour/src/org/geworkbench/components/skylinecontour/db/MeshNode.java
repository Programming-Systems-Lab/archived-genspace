package org.geworkbench.components.skylinecontour.db;

/**
 * $Rev$
 *
 * Represents a node in the Mesh Heirarchy.
 *
 * @author William Mee <wjm2107@cs.columbia.edu>
 * Copyright (c) 2008 The Trustees of Columbia University
 */
public class MeshNode {
	
	private String _id;
	private String _heading;
	
	public MeshNode(String heading, String id) {
		super();
		_heading = heading;
		_id = id;
	}

	public String getId() {
		return _id;
	}

	public void setId(String id) {
		_id = id;
	}

	public String getHeading() {
		return _heading;
	}

	public void setHeading(String heading) {
		_heading = heading;
	}
	
	
	
	

}
