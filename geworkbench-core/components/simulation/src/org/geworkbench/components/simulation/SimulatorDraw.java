    private void SBMLHandler(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SBMLHandler
        JFileChooser fileChooser = new JFileChooser();
        fileChooser.setFileFilter(new SBMLFilter());
        fileChooser.setCurrentDirectory(new File("data"));
        fileChooser.setSelectedFile(new File("sim_cytoscape.xml"));
        if (JFileChooser.APPROVE_OPTION == fileChooser.showSaveDialog((Component)evt.getSource())) {
            fileName = fileChooser.getSelectedFile().getPath();
            if (fileChooser.getSelectedFile().exists()) {
                int response = JOptionPane.showConfirmDialog(null,
                        "Overwrite existing file?","Confirm Overwrite",
                        JOptionPane.OK_CANCEL_OPTION,
                        JOptionPane.QUESTION_MESSAGE);
                if (response != JOptionPane.CANCEL_OPTION) {
                    cytoscape.createSBMLFile(fileName);
                    
                }
            } else {
                cytoscape.createSBMLFile(fileName); 
            }
        }
    }//GEN-LAST:event_SBMLHandler

