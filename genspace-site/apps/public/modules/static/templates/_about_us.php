					<h2 class="title">About genSpace</h2>
					
<p>
genSpace is a tool that allows geWorkbench users to collaborate and share knowledge using a social networking metaphor. 
It transparently logs information about the analysis tools that geWorkbench users run, so as to answer such questions as ``what are the most commonly used analysis tools?'' or ``which analysis tools are most commonly used together?'' These questions are answered by graphical visualizations and by real-time suggestions that are integrated into the geWorkbench environment. 
</p>

<p>
This web portal provides registered users with the ability to comment on and rate different analysis tools and series of analysis tools, called ``workflows''.
</p>	
					
