<?php



class NetworksMapBuilder {

	
	const CLASS_NAME = 'lib.model.map.NetworksMapBuilder';

	
	private $dbMap;

	
	public function isBuilt()
	{
		return ($this->dbMap !== null);
	}

	
	public function getDatabaseMap()
	{
		return $this->dbMap;
	}

	
	public function doBuild()
	{
		$this->dbMap = Propel::getDatabaseMap('propel');

		$tMap = $this->dbMap->addTable('networks');
		$tMap->setPhpName('Networks');

		$tMap->setUseIdGenerator(true);

		$tMap->addPrimaryKey('ID', 'Id', 'int', CreoleTypes::INTEGER, true, null);

		$tMap->addColumn('NETWORK', 'Network', 'string', CreoleTypes::VARCHAR, true, 50);

	} 
} 